<template>
  <div id="app"
       style="scroll-behavior: smooth;"
       @drop="drop_handler($event);"
       @dragover="dragover_handler($event);"
       @dragend="dragend_handler($event);">
    <!-- <cover></cover> -->
    <tiptap/>
    <!-- <router-view></router-view> -->
  </div>
